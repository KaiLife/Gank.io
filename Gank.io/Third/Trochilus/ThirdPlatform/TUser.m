#import "TUser.h"

@implementation TUser

static TUser* _instance = nil;

#pragma mark- 单例模式
+(instancetype) shareInstance
{
    static dispatch_once_t onceToken ;
    dispatch_once(&onceToken, ^{
        _instance = [[self alloc] init] ;
    }) ;
    
    return _instance ;
}

@end
